//1. addTwoNumbers
function add(num1 , num2){
    return num1+num2;
}
console.log("Addition is  "+add(12,10));

//2. subtractNumbers
function sub(num1 , num2){
    return num1-num2;
}
console.log("subtraction is  "+sub(12,10));

// 3. multiplyNumbers
function mul(num1 , num2){
    return num1*num2;
}
console.log("Multiplication is  "+ mul(12,10));

// 4. divideNumbers
function div(num1 , num2){
    return num1/num2;
}
console.log("Division is  "+div(12,10));
// 5. getRemainder
function rem(num1 , num2){
    return num1%num2;
}
console.log("Reminder is  "+rem(12,10));


